<?php

/*
|--------------------------------------------------------------------------
| Cross-Origin Resource Sharing (CORS) Configuration
|--------------------------------------------------------------------------
|
| Настройки CORS (Cross-Origin Resource Sharing) определяют, какие запросы
| из других доменов могут выполняться в браузере. Здесь можно указать
| разрешённые пути, методы, заголовки и другие параметры.
|
| Подробнее: https://developer.mozilla.org/en-US/docs/Web/HTTP/CORS
|
*/

return [

    /*
    | Разрешённые пути для CORS-запросов. Можно указать маршруты API,
    | а также любые другие маршруты, требующие кросс-доменных запросов.
    */
    'paths' => [
        'api/*',                // Все маршруты API
        'sanctum/csrf-cookie',  // Маршрут для CSRF-токена Sanctum
        'broadcasting/auth',    // Аутентификация для вещания (broadcast)
        'fire',                 // Пример дополнительного маршрута
    ],

    /*
    | Разрешённые HTTP-методы для кросс-доменных запросов.
    | ['*'] означает, что разрешены все методы.
    */
    'allowed_methods' => ['*'],

    /*
    | Разрешённые источники запросов (origins). ['*'] означает,
    | что любой домен может отправлять запросы.
    */
    'allowed_origins' => ['*'],

    /*
    | Разрешённые шаблоны источников запросов. Можно использовать
    | регулярные выражения для более гибкой настройки.
    */
    'allowed_origins_patterns' => [],

    /*
    | Разрешённые заголовки в CORS-запросах. ['*'] означает,
    | что любые заголовки разрешены.
    */
    'allowed_headers' => ['*'],

    /*
    | Заголовки, которые могут быть видимы клиенту (exposed headers).
    | По умолчанию пустой массив.
    */
    'exposed_headers' => [],

    /*
    | Максимальное время (в секундах), в течение которого браузер может
    | кешировать результаты предзапроса (preflight request).
    */
    'max_age' => 0,

    /*
    | Указывает, поддерживаются ли куки и авторизация (credentials)
    | при кросс-доменных запросах.
    */
    'supports_credentials' => true,

];
